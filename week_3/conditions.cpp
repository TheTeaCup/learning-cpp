#include <iostream>
using namespace std;
int main() {
    int num;
    cout << "Enter a number between 1 and 5: ";
    cin >> num;

    if (num == 1){
        cout << "1 is I";

    }

    if (num == 2){
        cout << "2 is II";
    }

    if (num == 3){
        cout << "3 is III";
    }

    if (num == 4){
        cout << "4 is IV";
    }

    if (num == 5){
        cout << "5 is V";
    }
}